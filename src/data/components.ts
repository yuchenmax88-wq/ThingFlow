  requiredLibs: string[]
  codeTemplate: {
    init: { zh: string; en: string }
    read?: { zh: string; en: string }
    write?: { zh: string; en: string }
  }
  needsExtraComponents?: { zh: string; en: string }
  isHighVoltage?: boolean
